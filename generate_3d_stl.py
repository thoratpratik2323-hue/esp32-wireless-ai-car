# 1. GENERATE CHASSIS PLATE STL
# -------------------------------------------------------------
def generate_chassis_stl():
    print("Generating Chassis Plate STL...")
    # Dimensions in mm
    L = 160.0  # Length
    W = 100.0  # Width
    H = 4.0    # Thickness (Strong for 3D printing)
    
    triangles = []
    
    # 8 vertices of a simple solid block chassis
    v = [
        (0.0, 0.0, 0.0),  # 0
        (L, 0.0, 0.0),    # 1
        (L, W, 0.0),      # 2
        (0.0, W, 0.0),    # 3
        (0.0, 0.0, H),    # 4
        (L, 0.0, H),      # 5
        (L, W, H),        # 6
        (0.0, W, H)       # 7
    ]
    
    # Add 6 faces (each face is a quad = 2 triangles)
    # Bottom
    add_quad(triangles, v[0], v[3], v[2], v[1])
    # Top
    add_quad(triangles, v[4], v[5], v[6], v[7])
    # Front
    add_quad(triangles, v[0], v[1], v[5], v[4])
    # Back
    add_quad(triangles, v[2], v[3], v[7], v[6])
    # Left
    add_quad(triangles, v[0], v[4], v[7], v[3])
    # Right
    add_quad(triangles, v[1], v[2], v[6], v[5])
    
    write_binary_stl("chassis_plate.stl", triangles)
    print("Saved 'chassis_plate.stl' successfully!")

