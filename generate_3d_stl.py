# 2. GENERATE WHEEL STL (Cylinder with a central axle hole)
# -------------------------------------------------------------
def generate_wheel_stl():
    print("Generating Wheel STL...")
    # Dimensions in mm
    R = 35.0   # Radius (70mm diameter)
    W = 15.0   # Width
    r_axle = 2.7  # 5.4mm axle radius (fits standard TT motors)
    
    segments = 36  # Number of polygons to form the circle
    triangles = []
    
    # Generating vertices for outer cylinder and inner axle hole
    outer_bottom = []
    outer_top = []
    inner_bottom = []
    inner_top = []
    
    for i in range(segments):
        angle = 2.0 * math.pi * i / segments
        cos_a = math.cos(angle)
        sin_a = math.sin(angle)
        
        # Outer wheel rim vertices
        outer_bottom.append((R * cos_a, R * sin_a, 0.0))
        outer_top.append((R * cos_a, R * sin_a, W))
        
        # Inner axle hole vertices
        inner_bottom.append((r_axle * cos_a, r_axle * sin_a, 0.0))
        inner_top.append((r_axle * cos_a, r_axle * sin_a, W))

    # Add outer side wall faces
    for i in range(segments):
        next_i = (i + 1) % segments
        add_quad(triangles, outer_bottom[i], outer_bottom[next_i], outer_top[next_i], outer_top[i])
        
    # Add inner side wall faces (axle hole) - faces inward
    for i in range(segments):
        next_i = (i + 1) % segments
        add_quad(triangles, inner_bottom[i], inner_top[i], inner_top[next_i], inner_bottom[next_i])

    # Add bottom cap (Donut shape between inner and outer circles)
    for i in range(segments):
        next_i = (i + 1) % segments
        # Triangle 1
        v1 = outer_bottom[i]
        v2 = inner_bottom[i]
        v3 = inner_bottom[next_i]
        n1 = compute_normal(v1, v2, v3)
        triangles.append((n1, v1, v2, v3))
        
        # Triangle 2
        v4 = outer_bottom[i]
        v5 = inner_bottom[next_i]
        v6 = outer_bottom[next_i]
        n2 = compute_normal(v4, v5, v6)
        triangles.append((n2, v4, v5, v6))

    # Add top cap (Donut shape)
    for i in range(segments):
        next_i = (i + 1) % segments
        # Triangle 1
        v1 = outer_top[i]
        v2 = outer_top[next_i]
        v3 = inner_top[next_i]
        n1 = compute_normal(v1, v2, v3)
        triangles.append((n1, v1, v2, v3))
        
        # Triangle 2
        v4 = outer_top[i]
        v5 = inner_top[next_i]
        v6 = inner_top[i]
        n2 = compute_normal(v4, v5, v6)
        triangles.append((n2, v4, v5, v6))

    write_binary_stl("wheel.stl", triangles)
    print("Saved 'wheel.stl' successfully!")

# Run generators
if __name__ == "__main__":
    generate_chassis_stl()
    generate_wheel_stl()
