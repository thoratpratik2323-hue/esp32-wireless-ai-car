import struct
import math
import os

def write_binary_stl(filename, triangles):
    """
    Writes a list of triangles to a Binary STL file.
    Each triangle in the list is a tuple/list of:
    ( (nx, ny, nz), (v1x, v1y, v1z), (v2x, v2y, v2z), (v3x, v3y, v3z) )
    """
    with open(filename, 'wb') as f:
        # 80-byte header
        header = b'STL generated by AI Smart Car Assistant'.ljust(80, b'\0')
        f.write(header)
        
        # Number of triangles (4-byte uint)
        num_triangles = len(triangles)
        f.write(struct.pack('<I', num_triangles))
        
        # Write each triangle
        for tri in triangles:
            normal = tri[0]
            v1 = tri[1]
            v2 = tri[2]
            v3 = tri[3]
            
            # Pack normal and vertices (12 floats)
            data = struct.pack('<12f', 
                               normal[0], normal[1], normal[2],
                               v1[0], v1[1], v1[2],
                               v2[0], v2[1], v2[2],
                               v3[0], v3[1], v3[2])
            f.write(data)
            # Attribute byte count (2 bytes, set to 0)
            f.write(struct.pack('<H', 0))

def compute_normal(v1, v2, v3):
    """Computes the normal vector for a triangle."""
    ux = v2[0] - v1[0]
    uy = v2[1] - v1[1]
    uz = v2[2] - v1[2]
    
    vx = v3[0] - v1[0]
    vy = v3[1] - v1[1]
    vz = v3[2] - v1[2]
    
    # Cross product
    nx = uy * vz - uz * vy
    ny = uz * vx - ux * vz
    nz = ux * vy - uy * vx
    
    # Normalize
    length = math.sqrt(nx*nx + ny*ny + nz*nz)
    if length == 0:
        return (0.0, 0.0, 1.0)
    return (nx/length, ny/length, nz/length)

def add_quad(triangles, v1, v2, v3, v4):
    """Helper to add a quad (4 vertices) as 2 triangles, maintaining clockwise orientation."""
    n1 = compute_normal(v1, v2, v3)
    triangles.append((n1, v1, v2, v3))
    
    n2 = compute_normal(v1, v3, v4)
    triangles.append((n2, v1, v3, v4))

